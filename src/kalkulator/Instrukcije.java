package kalkulator;
public class Instrukcije {
 
	public Instrukcije() {
	
	}
	//Metoda sabiranja
	public double saberi(double a, double b){	
		return (a+b);
	}
	//Metoda oduzimanja
	public double oduzmi(double a, double b){
		return (a-b);
	}
	//Metoda mnozenja

	//Metoda djeljenja
	
	//Metoda korjenovanje
	public double korijen(double a){
		return Math.sqrt(a);
	}
	
	//Metoda kvadriranje
	public double kvadrat(double a){
		return (a*a); 
		
	}
}
